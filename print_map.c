#include "fillit.h"

void	print_map(t_map *map)
{
	int x;
	int y;

	y = 0;
	while (y < map->size)
	{
		x = 0;
		while (x < map->size)
		{
			write(1, &(map->map[y][x]), 1);
			x++;
		}
		write(1, "\n", 1);
		y++;
	}
}
